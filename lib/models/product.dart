class Product {
  final String name;
  final String description;
  final String imageUrl;
  final double price;
  final String category;
  final String shopName;
  final int stock;

  Product({
    required this.name,
    required this.description,
    required this.imageUrl,
    required this.price,
    required this.category,
    required this.shopName,
    required this.stock,
  });
}
